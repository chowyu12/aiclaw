const messages: Record<Language, Messages> = {
  'zh-CN': zh,
  'en-US': en,
}

export function applyDomLanguage(lang: Language) {
  document.documentElement.lang = lang
}

export function readInitialLanguage(): Language {
  try {
    const raw = localStorage.getItem('aiclaw-language')
    if (raw == null) return 'zh-CN'
    const v = JSON.parse(raw) as string
    return v === 'en-US' ? 'en-US' : 'zh-CN'
  } catch {
    return 'zh-CN'
  }
}

export const useI18nStore = defineStore('i18n', () => {
  const language = useStorage<Language>('aiclaw-language', 'zh-CN')

  const isEnglish = computed(() => language.value === 'en-US')

  watch(language, (lang) => applyDomLanguage(lang), { immediate: true })

  function setLanguage(lang: Language) {
    language.value = lang
  }

  function toggleLanguage() {
    language.value = language.value === 'zh-CN' ? 'en-US' : 'zh-CN'
  }

  function t(key: string, params?: Record<string, string | number>): string {
    let text = messages[language.value][key] ?? messages['zh-CN'][key] ?? key
    if (params) {
      for (const [name, value] of Object.entries(params)) {
        text = text.split(`{${name}}`).join(String(value))
      }
    }
    return text
  }

  return { language, isEnglish, setLanguage, toggleLanguage, t }
})
